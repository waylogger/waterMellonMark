void Window::draw() { for ( long unsigned int i = 0; i < elements.size(); ++i) {this->draw_spec();wnd->draw(*elements[i]); } }
//-------------------------------------------------------------------------------------------------
void Window::show() {

	while (wnd->isOpen()) {
	sf::Event event;
		while (wnd->pollEvent(event))   {if (event.type == sf::Event::Closed) wnd->close();}
	wnd->clear();
	this->draw();
	wnd->display();

	}
}
//-------------------------------------------------------------------------------------------------
void Window::attach(sf::Drawable* d) { this->elements.push_back(d); }
//-------------------------------------------------------------------------------------------------
Window::Window(const double w, const double h, const std::string& l) {
			this->wth = w;
			this->hth = h;
			this->lbl = l;
			this->wnd = new sf::RenderWindow(sf::VideoMode(this->wth,this->hth),this->lbl);
		}
